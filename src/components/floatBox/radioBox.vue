<template>
  <scroll-view scroll-x="true">
    <view class="big-box">
      <view class="option-container light form-control" @touchstart="touchStart(option)" @touchmove="touchMove" @touchend="touchEnd" @touch="touchOn" @click="selectItem(option)" v-for="option in list.items" :key="option" :class="{'have background-fff': !havaImage, 'no-image user-msg-box-color': havaImage}">
        <block v-if="!havaImage">
          <view class="image-box imageBox">
            <image class="image" mode="aspectFit" :src="option.imageUrl" v-if="option.imageUrl"></image>
          </view>
          <view class="value image-value" v-if="option.caption">{{option.caption}}</view>
        </block>
        <block v-else>
          <view class="value valueBox">{{option.caption}}</view>
        </block>
      </view>
    </view>
  </scroll-view>

</template>

<script>
  export default {
    data () {
      return {
        value: '',
        touchStartTime: '',
        touchEndTime: '',
        touchMoveTime: '',
        timeout: ''
      }
    },
    name: 'radioBox',
    props: ['list'],
    computed: {
      havaImage: state => {
        let a = state.list.items.find(item => !!item.imageUrl === true)
        return a === undefined
      }
    },
    methods: {
      selectItem (obj) {
        if (this.touchEndTime - this.touchStartTime < 800) {
          this.$store.dispatch('sentRadioReply', {...obj, value: obj.value ? obj.value : obj.caption})
        }
      },
      touchStart (option) {
        if (this.timeout) {
          clearTimeout(this.timeout)
        }
        const that = this
        this.touchStartTime = new Date().getTime()
        this.timeout = setTimeout(function () {
          if (option.imageUrl) {
            that.$store.commit('setPreviewFalse')
            wx.previewImage({
              current: option.imageUrl,
              urls: [option.imageUrl]
            })
          }
        }, 800)
      },
      touchMove () {
        this.touchMoveTime = new Date().getTime()
        if (this.touchMoveTime - this.touchStartTime < 800) {
          clearTimeout(this.timeout)
          this.timeout = ''
        }
      },
      touchEnd () {
        this.touchEndTime = new Date().getTime()
        if (this.touchEndTime - this.touchStartTime < 800) {
          clearTimeout(this.timeout)
          this.timeout = ''
        }
      }
    }
  }
</script>

